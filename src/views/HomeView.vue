<script>
import axios from 'axios'

export default {
  name: 'HomeView',
  components: {

  },
  data: function() {
    return {
      usuario: "",
      contraseña: "",
      error: false,
      mensaje: ""
    }
  },
  methods: {
    login(){
      let json = {
        "email": this.usuario,
        "password": this.contraseña
      };
      axios.post('https://crm-utp.herokuapp.com/api/auth/login', json)
      .then(data => {
        const { msg } = data.data
        if(msg === "Acceso concedido"){
          console.log("Todo ok")
        }
      })
      .catch(err => {
        const { msg } = err.response.data
        if(msg !== "Acceso concedido"){
          //console.log(msg)
          this.error = true
          this.mensaje = msg
        }
      })
    }
  }
}

</script>

